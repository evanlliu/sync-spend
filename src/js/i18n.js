let currentLanguage = localStorage.getItem("syncSpend.lang") || "zh-CN";

export function setLanguage(lang) {
  currentLanguage = dict[lang] ? lang : "zh-CN";
  localStorage.setItem("syncSpend.lang", currentLanguage);
  document.documentElement.lang = currentLanguage;
}

export function getLanguage() {
  return currentLanguage;
}

export function t(key, vars = {}) {
  const value = dict[currentLanguage]?.[key] || dict["zh-CN"][key] || key;
  return Object.entries(vars).reduce((text, [name, val]) => text.replaceAll(`{${name}}`, String(val)), value);
}

export function localizedName(value, fallback = "") {
  if (!value) return fallback;
  if (typeof value === "string") return value;
  return value[currentLanguage] || value["zh-CN"] || value["en-US"] || fallback;
}
